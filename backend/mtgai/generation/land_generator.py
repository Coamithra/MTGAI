def _make_basic_card(
    basic: dict, flavor_text: str, set_code: str, collector_number: str, scene: str = ""
) -> Card:
    """One basic-land printing. ``scene`` is the alternate's art brief, stored in
    ``design_notes`` so the art-prompt stage (which feeds design_notes to the LLM)
    renders a distinct vista per alternate."""
    notes = f"Alternate basic land art — {scene}" if scene else "Basic land"
    return Card(
        name=basic["name"],
        mana_cost=None,
        cmc=0.0,
        colors=[],
        color_identity=[basic["color_identity"]],
        type_line=f"Basic Land — {basic['subtype']}",
        supertypes=["Basic"],
        card_types=["Land"],
        subtypes=[basic["subtype"]],
        oracle_text="",
        flavor_text=flavor_text,
        collector_number=collector_number,
        rarity=Rarity.COMMON,
        set_code=set_code,
        is_reprint=True,
        design_notes=notes,
        created_at=datetime.now(UTC),
        updated_at=datetime.now(UTC),
    )


def _make_nonbasic_card(data: dict, set_code: str) -> Card:
    """Build the bonus dual land from the investigation's design. A from-scratch
    RARE (``is_reprint=False``); ``color_identity`` is inferred from the mana
    symbols in its oracle text."""
    ci: list[Color] = []
    color_map = {
        "{W}": Color.WHITE,
        "{U}": Color.BLUE,
        "{B}": Color.BLACK,
        "{R}": Color.RED,
        "{G}": Color.GREEN,
    }
    for symbol, color in color_map.items():
        if symbol in data["oracle_text"]:
            ci.append(color)

    return Card(
        name=data["name"],
        mana_cost=None,
        cmc=0.0,
        colors=[],
        color_identity=ci,
        type_line=data["type_line"],
        supertypes=[],
        card_types=["Land"],
        subtypes=[],
        oracle_text=data["oracle_text"],
        flavor_text=data["flavor_text"],
        collector_number="L-06",
        rarity=Rarity.RARE,
        set_code=set_code,
        is_reprint=False,
        design_notes="Bonus rare dual/fixing land added by the lands investigation",
        created_at=datetime.now(UTC),
        updated_at=datetime.now(UTC),
    )


def _slugify(name: str) -> str:
    return name.lower().replace(" ", "_").replace("'", "").replace(",", "")


# ---------------------------------------------------------------------------
# Stage entry point
# ---------------------------------------------------------------------------


def generate_lands(
    on_call_start: Callable[[str], None] | None = None,
    on_card_saved: Callable[[Card], None] | None = None,
) -> dict:
    """Generate land cards for the active project: 2-4 alternate-art printings per
    basic type, plus one investigated bonus dual land if the set's fixing needs it.

    Returns a summary dict with ``total_cards`` and ``cost_usd``.

    Optional callbacks let a UI surface progress:
      ``on_call_start(model)`` fires before each AI request (basics, then
      investigation), ``on_card_saved(card)`` fires after each Card is written.
    """
    from mtgai.io.asset_paths import set_artifact_dir
    from mtgai.runtime.active_project import require_active_project

    project = require_active_project()
    set_code = project.set_code
    set_dir = set_artifact_dir()
    skeleton_path = set_dir / "skeleton.json"
    cards_dir = set_dir / "cards"
    cards_dir.mkdir(parents=True, exist_ok=True)

    # Clear this stage's prior output first. The lands stage owns the ``L-*`` cards
    # (basics + dual); card-gen's land cycles use numeric collector numbers, so the
    # ``L-*`` glob never touches them. Without this, a re-run (e.g. via the edit
    # cascade) would leave stale alternates behind — the random per-type count and
    # lettered numbers (L-01a/b/c) mean a fresh run won't overwrite the old ones.
    for stale in cards_dir.glob("L-*.json"):
        stale.unlink()

    set_config = extract_set_config(skeleton_path)
    # Shared set-context blocks (setting prose / constraints / mechanics / archetypes)
    # read from theme.json — used by BOTH the basics call and the fixing investigation
    # so each judges the same set framing the skeleton relabel saw.
    context = _load_set_context(set_dir, set_config)
    model_id = project.settings.get_llm_model_id("lands")
    # Route both LLM transcripts (JSONL + HTML) beside the stage artifacts, like
    # every other stage (mechanics/logs, reprints/logs, …) — omitting log_dir
    # would dump them into llmfacade's session dirs under backend/logs/.
    log_dir = set_dir / "lands" / "logs"

    logger.info("Generating lands for set %s...", set_code)

    cards_saved = 0
    cost = 0.0

    # (1) Basic land flavor — one call.
    system, user = _build_basics_prompt(set_config, context)
    if on_call_start is not None:
        on_call_start(model_id)
    response = generate_with_tool(
        system_prompt=system,
        user_prompt=user,
        tool_schema=_build_basics_tool_schema(),
        model=model_id,
        temperature=0.7,
        # 5 land types x (4 one-sentence art briefs + 1 flavor) is a lot of JSON;
        # at 2048 the (often verbose, local) model truncated mid-output
        # (finish: length) around the 3rd type. 4096 fits all five with headroom.
        max_tokens=4096,
        log_dir=log_dir,
    )
    basics_data = response.get("result", {}).get("basics", [])
    cost += cost_from_result(response)
    logger.info(
        "Basics tokens: in=%s out=%s",
        response.get("input_tokens", "?"),
        response.get("output_tokens", "?"),
    )

    by_name = {str(b.get("land_name", "")): b for b in basics_data if isinstance(b, dict)}
    for basic in BASICS:
        data = by_name.get(basic["name"], {})
        scenes = [str(s).strip() for s in (data.get("art_scenes") or []) if str(s).strip()]
        flavor = str(data.get("flavor_text") or "").strip()

        # 2-4 alternate-art printings per basic type (modern-set style). The count
        # is random per type, capped by how many distinct scenes the model gave us.
        count = random.randint(2, 4)
        if scenes:
            count = min(count, len(scenes))
        else:
            logger.warning("No art scenes for %s; emitting a single plain basic", basic["name"])
            scenes, count = [""], 1

        for j in range(count):
            # Flavor on at most ONE printing per type (the first); the rest are
            # flavorless — basics rarely carry flavor text.
            variant_flavor = flavor if j == 0 else ""
            cn = f"{basic['cn']}{chr(ord('a') + j)}"  # L-01a, L-01b, …
            card = _make_basic_card(basic, variant_flavor, set_code, cn, scenes[j])
            filename = f"{cn}_{_slugify(basic['name'])}.json"
            atomic_write_text(
                cards_dir / filename,
                json.dumps(card.model_dump(mode="json"), indent=2, ensure_ascii=False),
            )
            cards_saved += 1
            logger.info("  Saved %s alt %s: %s", basic["name"], cn, filename)
            if on_card_saved is not None:
                on_card_saved(card)

    # Honor a Cancel between the two LLM calls — an in-flight call can't be
    # interrupted, so we stop at this boundary. The basics already written persist;
    # the guarded endpoint reads ``ai_lock.is_cancelled()`` to skip the success-heal.
    if ai_lock.is_cancelled():
        logger.warning(
            "Land generation CANCELLED by user after basics (%d cards saved)", cards_saved
        )
        return {"total_cards": cards_saved, "cost_usd": cost, "cancelled": True}

    # (2) Investigate the set's fixing across all three sources, add one bonus
    # rare dual land only if a real gap remains. Reuses the ``context`` loaded above.
    # Exclude reprint-stamped slots: the reprint stage already claimed them (they're
    # listed separately as "reprints placed"), so they're no longer unfilled.
    slot_texts = _load_slot_texts(skeleton_path, include_reprints=False)
    reprint_summary = _load_reprint_summary(set_dir)
    inv_system, inv_user = _build_investigation_prompt(
        set_config, context, slot_texts, reprint_summary
    )
    if on_call_start is not None:
        on_call_start(model_id)
    inv_result: dict = {}
    try:
        inv_response = generate_with_tool(
            system_prompt=inv_system,
            user_prompt=inv_user,
            tool_schema=_build_investigation_tool_schema(),
            model=model_id,
            temperature=0.7,
            # Smaller output (one dual + reasoning), but a truncated tool-call JSON
            # fails to parse just the same — give the same headroom as the basics call.
            max_tokens=4096,
            log_dir=log_dir,
        )
        cost += cost_from_result(inv_response)
        inv_result = inv_response.get("result", {}) or {}
    except Exception:
        logger.exception("Land fixing investigation failed; adding no bonus dual")

    dual = inv_result.get("dual_land")
    reasoning = str(inv_result.get("reasoning") or "").strip()
    if inv_result.get("needs_dual_land") and isinstance(dual, dict) and dual.get("name"):
        card = _make_nonbasic_card(dual, set_code)
        filename = f"L-06_{_slugify(dual['name'])}.json"
        atomic_write_text(
            cards_dir / filename,
            json.dumps(card.model_dump(mode="json"), indent=2, ensure_ascii=False),
        )
        cards_saved += 1
        if on_card_saved is not None:
            on_card_saved(card)
        logger.info("  Added bonus dual land: %s — %s", dual["name"], reasoning)
    else:
        logger.info("Investigation declined a bonus dual land: %s", reasoning or "(no reason)")

    logger.info("Generated %d land cards ($%.4f)", cards_saved, cost)
    return {"total_cards": cards_saved, "cost_usd": cost}
