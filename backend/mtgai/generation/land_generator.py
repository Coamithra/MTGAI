"""Land generation — 5 basic lands + (optionally) one investigated dual land.

The `lands` stage does two things: (1) write the 5 basic lands with set-flavored
text, and (2) run a single LLM **investigation** of the set's mana fixing that adds
**one** from-scratch rare dual/fixing land *only when* a real gap remains. Fixing is
decided in three places, in order — reserved **cycles** in the skeleton step (e.g. a
guildgate cycle, now generated by card-gen), mana-fixing **reprints** chosen in the
reprint step, and finally this investigation as the last word. It is given visibility
into the first two so the bonus dual is a genuine top-up, not a duplicate.

Land *cycles* are no longer generated here — they're ordinary cycle members in the
skeleton and are designed by card-gen alongside every other card.

Uses two cheap calls on the active project's ``lands`` model (Haiku/local Gemma).
"""

from __future__ import annotations

import json
import logging
import random
from collections.abc import Callable
from datetime import UTC, datetime
from pathlib import Path

from mtgai.generation.llm_client import cost_from_result, generate_with_tool
from mtgai.generation.reprint_selector import (
    _load_set_context,
    _load_slot_texts,
    _read_json,
    extract_set_config,
)
from mtgai.io.atomic import atomic_write_text
from mtgai.models.card import Card
from mtgai.models.enums import Color, Rarity
from mtgai.runtime import ai_lock

logger = logging.getLogger(__name__)

BASICS = [
    {"name": "Plains", "subtype": "Plains", "color_identity": Color.WHITE, "cn": "L-01"},
    {"name": "Island", "subtype": "Island", "color_identity": Color.BLUE, "cn": "L-02"},
    {"name": "Swamp", "subtype": "Swamp", "color_identity": Color.BLACK, "cn": "L-03"},
    {"name": "Mountain", "subtype": "Mountain", "color_identity": Color.RED, "cn": "L-04"},
    {"name": "Forest", "subtype": "Forest", "color_identity": Color.GREEN, "cn": "L-05"},
]


# ---------------------------------------------------------------------------
# Task 1: basic land flavor text
# ---------------------------------------------------------------------------


def _build_basics_prompt(set_config: dict, context: dict[str, str]) -> tuple[str, str]:
    system = (
        "You are a Magic: The Gathering creative writer and art director. You write "
        "evocative, setting-appropriate basic-land art briefs and (sparingly) flavor text."
    )
    # The setting prose + constraints come from the shared context blocks (read from
    # theme.json), NOT the skeleton config's legacy theme/flavor_description fields —
    # those are empty for theme.json-driven sets, which left this prompt blank.
    user = (
        f"# Basic Lands for {set_config.get('name', 'Unknown')}\n\n"
        "## Setting\n"
        f"{context['setting']}\n\n"
        "## Constraints\n"
        f"{context['constraints']}\n\n"
        "Modern sets print several ALTERNATE ARTS of each basic land — different vistas "
        "of the same world. For EACH of the five basic land types (Plains, Island, Swamp, "
        "Mountain, Forest), give:\n\n"
        "1. **art_scenes** — exactly 4 DISTINCT one-sentence art briefs. Each is a "
        "different location, landmark, weather, or time of day in THIS world and becomes "
        "one alternate printing, so make them visually distinct from one another. "
        "Panoramic and concrete, NO figures or characters, evoke a specific place. Match "
        "the terrain to the type (Plains = open/flat, Island = water/coast, Swamp = "
        "dark/decaying, Mountain = volcanic/rocky, Forest = overgrown wilderness).\n"
        "2. **flavor_text** — ONE short (1 sentence) flavor text for the type. Most "
        "printings carry no flavor, so make this one quotable and in-world; use an "
        "em-dash for attribution if quoting a character.\n"
    )
    return system, user


def _build_basics_tool_schema() -> dict:
    return {
        "name": "create_basic_lands",
        "description": "Provide alternate-art briefs + one flavor text for the 5 basic land types",
        "input_schema": {
            "type": "object",
            "properties": {
                "basics": {
                    "type": "array",
                    "description": (
                        "One entry per basic land type (Plains, Island, Swamp, Mountain, Forest)"
                    ),
                    "items": {
                        "type": "object",
                        "properties": {
                            "land_name": {
                                "type": "string",
                                "description": "Plains, Island, Swamp, Mountain, or Forest",
                            },
                            "art_scenes": {
                                "type": "array",
                                "description": (
                                    "4 distinct one-sentence art briefs (alternate arts) "
                                    "for this land type"
                                ),
                                "items": {"type": "string"},
                            },
                            "flavor_text": {
                                "type": "string",
                                "description": "One flavor text for this type (used on a single "
                                "printing)",
                            },
                        },
                        "required": ["land_name", "art_scenes", "flavor_text"],
                    },
                },
            },
            "required": ["basics"],
        },
    }


# ---------------------------------------------------------------------------
# Task 2: investigate the set's fixing, add one bonus dual only if needed
# ---------------------------------------------------------------------------


def _load_reprint_summary(asset: Path) -> str:
    """One line per chosen reprint (name · type_line · role) for the investigation
    prompt — the full placed-reprint list, so the model can judge which (if any)
    supply mana fixing (Evolving Wilds, taplands, fetch-a-basic ramp…) and which are
    just removal/creatures. Graceful when ``reprint_selection.json`` is absent/empty —
    the reprints stage may have been skipped."""
    data = _read_json(asset / "reprint_selection.json", {})
    selections = data.get("selections") if isinstance(data, dict) else None
    lines: list[str] = []
    for sel in selections or []:
        if not isinstance(sel, dict):
            continue
        cand = sel.get("candidate") or {}
        name = str(cand.get("name") or "").strip()
        if not name:
            continue
        bits = " · ".join(p for p in (cand.get("type_line", ""), cand.get("role", "")) if p)
        lines.append(f"- {name}" + (f" ({bits})" if bits else ""))
    if not lines:
        return "(no reprints were chosen for this set — assume no reprint-based fixing)"
    return "\n".join(lines)


def _build_investigation_prompt(
    set_config: dict,
    context: dict[str, str],
    slot_texts: list[dict[str, str]],
    reprint_summary: str,
) -> tuple[str, str]:
    """The set's full context + every unfilled slot + the chosen reprints → decide
    whether to add ONE bonus rare dual land. Reuses the shared skeleton-block
    formatters (via ``context``) so this judges the set the same way the relabel did.
    Multicolor density is read from the slot prose, not stale structured fields."""
    system = (
        "You are a Magic: The Gathering set designer making the FINAL call on a set's "
        "mana fixing. Fixing in a set comes from three sources, in order: (1) reserved "
        "land/card CYCLES in the skeleton (e.g. a guildgate cycle), (2) mana-fixing "
        "REPRINTS chosen from a staple pool (Evolving Wilds, taplands, etc.), and (3) "
        "you — a single bespoke BONUS land that tops up only if the first two left a "
        "gap.\n\n"
        "Your job: look at the whole set and decide whether it needs ONE extra "
        "dual/fixing land. Add one ONLY IF a real multicolor-fixing gap remains after "
        "the cycles and reprints above, OR the set has a strong multicolor theme that "
        "justifies a marquee dual on top. If the set already has adequate fixing, or is "
        "mostly mono-color, decline — do not add a redundant land.\n\n"
        "If you add one, design it as a RARE: a premium, setting-appropriate dual that "
        "need NOT enter tapped (it is a deliberate bonus, not baseline Limited fixing). "
        "Give it a setting-appropriate but generic name (no character names) and a "
        "one-sentence flavor text. Return your decision through the tool only."
    )

    slot_lines = "\n".join(f"{s['slot_id']}: {s['text']}" for s in slot_texts) or "(no slots)"
    user = (
        f"# Fixing investigation — {set_config.get('name', 'Unknown')}\n\n"
        "## Set theme\n"
        f"{context['setting']}\n\n"
        "## Mechanics\n"
        f"{context['mechanics']}\n\n"
        "## Draft archetypes\n"
        f"{context['archetypes']}\n\n"
        "## Constraints\n"
        f"{context['constraints']}\n\n"
        "## Reprints already placed in this set\n"
        "These staples already occupy skeleton slots — judge which of them (if any) "
        "supply mana fixing, and treat the rest as ordinary cards:\n"
        f"{reprint_summary}\n\n"
        f"## Unfilled skeleton slots ({len(slot_texts)})\n"
        "Reprint-claimed slots are excluded (see above). Land cycles reserved in the "
        "skeleton (e.g. guildgates) appear here as land slots; judge multicolor "
        "density and existing fixing from these descriptors.\n"
        f"{slot_lines}\n\n"
        "Decide: does this set need one bonus rare dual land on top of the fixing "
        "above? If yes, design it."
    )
    return system, user


def _build_investigation_tool_schema() -> dict:
    return {
        "name": "investigate_fixing",
        "description": "Decide whether the set needs one bonus dual land, and design it if so.",
        "input_schema": {
            "type": "object",
            "properties": {
                "needs_dual_land": {
                    "type": "boolean",
                    "description": "True only if a real fixing gap remains, or a "
                    "strong multicolor theme justifies a marquee dual.",
                },
                "reasoning": {
                    "type": "string",
                    "description": "Why the set does or does not need a bonus dual, citing the "
                    "existing cycle/reprint fixing.",
                },
                "dual_land": {
                    "type": "object",
                    "description": "The bonus rare dual land — provide ONLY when "
                    "needs_dual_land is true.",
                    "properties": {
                        "name": {"type": "string"},
                        "type_line": {"type": "string", "description": "e.g. 'Land'"},
                        "oracle_text": {"type": "string"},
                        "flavor_text": {"type": "string"},
                    },
                    "required": ["name", "type_line", "oracle_text", "flavor_text"],
                },
            },
            "required": ["needs_dual_land", "reasoning"],
        },
    }


# ---------------------------------------------------------------------------
# Card builders
